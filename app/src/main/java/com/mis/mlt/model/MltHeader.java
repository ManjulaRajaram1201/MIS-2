package com.mis.mlt.model;

public class MltHeader {
int docNo;
String desc;
int day;
int month;
int year;
int addCost;
public Integer getDocNo() {
	return docNo;
}
public void setDocNo(Integer docNo) {
	this.docNo = docNo;
}
public String getDesc() {
	return desc;
}
public void setDesc(String desc) {
	this.desc = desc;
}
public int getDay() {
	return day;
}
public void setDay(int day) {
	this.day = day;
}
public int getMonth() {
	return month;
}
public void setMonth(int month) {
	this.month = month;
}
public int getYear() {
	return year;
}
public void setYear(int year) {
	this.year = year;
}
public int getAddCost() {
	return addCost;
}
public void setAddCost(int addCost) {
	this.addCost = addCost;
}
}
