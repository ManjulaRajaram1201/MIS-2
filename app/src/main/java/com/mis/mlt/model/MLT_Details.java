package com.mis.mlt.model;

public class MLT_Details {
	
	String mlt_itemno;
	String mlt_description;
	String mlt_from;
	String mlt_to;
	String mlt_qty;
	String mlt_uom;
	String doc;
	String result;
	
	public MLT_Details()
	{
		
	}
	public String getDoc() {
		return doc;
	}
	public void setDoc(String doc) {
		this.doc = doc;
	}
	public String getMlt_itemno() {
		return mlt_itemno;
	}
	public void setMlt_itemno(String mlt_itemno) {
		this.mlt_itemno = mlt_itemno;
	}
	public String getMlt_description() {
		return mlt_description;
	}
	public void setMlt_description(String mlt_description) {
		this.mlt_description = mlt_description;
	}
	public String getMlt_from() {
		return mlt_from;
	}
	public void setMlt_from(String mlt_from) {
		this.mlt_from = mlt_from;
	}
	public String getMlt_to() {
		return mlt_to;
	}
	public void setMlt_to(String mlt_to) {
		this.mlt_to = mlt_to;
	}
	public String getResult() {
		return result;
	}
	public void setResult(String result) {
		this.result = result;
	}
	public String getMlt_qty() {
		return mlt_qty;
	}
	public void setMlt_qty(String mlt_qty) {
		this.mlt_qty = mlt_qty;
	}
	public String getMlt_uom() {
		return mlt_uom;
	}
	public void setMlt_uom(String mlt_uom) {
		this.mlt_uom = mlt_uom;
	}


}
