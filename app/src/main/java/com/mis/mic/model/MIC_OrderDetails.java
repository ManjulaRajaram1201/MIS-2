package com.mis.mic.model;

public class MIC_OrderDetails {
	String itemNumber;
	String itemDescription;
	String pickSeq;
	String qtyonHand;
	String qtyCount;
	String unit;
	String result;
	public String getItemNumber() {
		return itemNumber;
	}
	public void setItemNumber(String itemNumber) {
		this.itemNumber = itemNumber;
	}
	public String getItemDescription() {
		return itemDescription;
	}
	public void setItemDescription(String itemDescription) {
		this.itemDescription = itemDescription;
	}
	public String getPickSeq() {
		return pickSeq;
	}
	public void setPickSeq(String pickSeq) {
		this.pickSeq = pickSeq;
	}
	public String getQtyonHand() {
		return qtyonHand;
	}
	public void setQtyonHand(String qtyonHand) {
		this.qtyonHand = qtyonHand;
	}
	public String getQtyCount() {
		return qtyCount;
	}
	public void setQtyCount(String qtyCount) {
		this.qtyCount = qtyCount;
	}
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	public String getResult() {
		return result;
	}
	public void setResult(String result) {
		this.result = result;
	}
	
	


}
